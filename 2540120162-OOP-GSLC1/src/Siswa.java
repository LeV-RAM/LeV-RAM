
public class Siswa {
	String nama;
	int nilai;
	
	public Siswa(String nama, int nilai) {
		this.nama = nama;
		this.nilai = nilai;
	}
	
	public String getName() {
		return nama;
	}
	
	public void setName(String nama) {
		this.nama = nama;
	}
	
	public int getNilai() {
		return nilai;
	}
	
	public void setNilai(int nilai) {
		this.nilai = nilai;
	}
}
